function renderSections(sections) {
  return sections
    .map((s) => {
      let html = `<h2>${esc(s.heading)}</h2>`;
      if (s.paragraphs) html += s.paragraphs.map((p) => `<p>${esc(p)}</p>`).join('');
      if (s.list) html += `<ol class="legal-list">${s.list.map((li) => `<li>${esc(li)}</li>`).join('')}</ol>`;
      return html;
    })
    .join('\n          ');
}

